            <div class="blog-content">
            
				<p>With everything that is happening right now you're probably spending a lot more time at home than you are used to. It can be easy to start feeling distracted and demotivated, with second breakfast now a structured part of your day and joggers your official uniform, it's hard to commit to anything productive.&nbsp;</p>
<p>To help spread the positivity we have gathered our top tips to finding the right balance whilst at home. From the fun of Instagram yoga to the seriousness of scheduling snack breaks during assignments, we run through everything you need to know to keep smiling. Make sure you check out the end of the blog for advice from&nbsp;Joseph Morrell- University of Cumbria Students Union Welfare and Support Officer and a link to another wellbeing blog by our Mental Health and Wellbeing Manager, David Wilson.</p>
<h2>Schedule the important stuff, like snack breaks</h2>
<p>Try to give yourself a daily routine and follow it as best you can, making sure you have dedicated time to rest. Take as many snack breaks as you need and remember it is okay to give yourself a day off to focus on relaxing and having fun. Aim to make progress little and often so that any stress gradually reduces.</p>
<p>If you have assignments to complete, schedule in some time to get your head down and work. Jan Ashbridge, Principal Lecturer in Education spoke to her current students on how they are continuing their learning at home through online classes and the support of lecturers via Skype, phone or email. They recommend keeping in touch with your personal tutors to talk about any concerns and&nbsp;doing work from the comfort of your sofa.&nbsp;</p>
<p>Some students have started making their own wall planners marking on key dates and keeping on track with projects and assignments. If you prefer to keep things digital, you can download the free app "My Study Life" and tailor your academic timetable to suit you, set reminders and manage your workload from your phone or computer.</p>
<p><a title="My Study Life" href="https://www.mystudylife.com/%20"><img src="/media/My-Study-Life-Screenshot.png" alt="My Study Life Screenshot, Screenshot of homepage for my study life" style="width : 1343px; height : 653px;     " /></a></p>
<h2>Get fresh air and keep your body moving</h2>
<p>Schedule in regular breaks, including time outside whether this is your daily exercise or time spent in your garden. Fresh air is really positive for your mental health and exercise such as running, walking or structured workouts release happy endorphins.</p>
<p>You can follow along to yoga and HIIT workouts on Instagram by searching #yogaworkout or #hiitworkout, or if you fancy something less intense, Facetime your friends or family whilst taking a walk around your local area.</p>
<p><img src="/media/Workout-Feed.png" alt="Workout Feeds, screenshot of workout feeds on Instagram" style="width : 2384pxpx; height : 1860pxpx;     " /></p>
<h2>Feed your body and mind the good stuff</h2>
<p>Senior Lecturer in Applied Psychology Susan Wilbraham highlighted that caffeine and sugary food can amplify feelings of anxiety or stress so it is important if you are feeling down, to cut back on your caffeine/sugar consumption. Try to feed your body and mind with healthy, nutritious food. If you're struggling for inspiration head over to Instagram or Pinterest and search "healthy food" for the latest recipes.</p>
<p>Once you've eaten lunch by 10am, scrolled so far on Instagram that flares are back in and binged the whole of Love is Blind it might be time for some headspace relaxation. Download the app Headspace on your phone and access 10 free sessions that you can replay at any time.</p>
<p><a title="Headspace" href="https://www.headspace.com/"><img src="/media/Headspace-homepage-screenshot.png" alt="Headspace website screenshot, screenshot of Headspace homepage" style="width : 1347px; height : 597px;     " /></a></p>
<h2>Positivity is key</h2>
<p>Social media can provide a welcome break so it is okay to immerse yourself in dog videos and the latest TikTok dance trend. You can also follow accounts such as @thehappynewspaper on Instagram for your daily dose of positivity.</p>
<p>Try to limit the amount of Covid-19 related content you are exposed to, especially before bed. However, we understand that it is important to stay up to date. When reading about the latest updates use trusted sources such as the NHS, the government and trusted news sources only. Try to avoid hearsay or speculation from personal or unofficial accounts as this can often heighten your anxiety and the information is not always accurate.</p>
<p>To help keep things positive and fun, our social accounts will continue to post, from your favourite lecturers giving talks, to hosting workouts and supporting our community, we're here to spread the good feelings.</p>
<p><a title="The Happy Newspaper" href="https://thehappynewspaper.com/"><img src="/media/The-Happy-Newspaper-Instagram-Post.png" alt="The Happy Newspaper, Screenshot of the happy newspaper instagram" style="width : 929px; height : 535px;     " /></a></p>
<h2><strong>It's okay to talk</strong></h2>
<p>It is okay to feel worried, anxious or stressed. It is important to talk about your feelings with your friends, family or tutors. This can help you make sense of your thoughts and will help you feel better. A problem shared is a problem halved and your worries are no different. We offer various support services to everyone in the University of Cumbria family to help guide and support you  find out more <a href="/student-life/student-support/student-development-and-support/">here.</a></p>
<p>You can also start a gratitude journal to help you focus on the positives in your life. It is simple and easy to do, just find a spare notebook or paper and number 1 to 3. Then write down 3 things you are grateful for today, don't worry it doesn't have to be ground-breaking, a tasty breakfast or a sunny walk are all things to be grateful for. Try this every day, share your thoughts with your friends and family and to help spread the gratitude.</p>
<p><iframe src="https://www.youtube.com/embed/AHS7UtmQBpw" width="560" height="315" frameborder="0" allowfullscreen=""></iframe></p>
<h2>Self-care is important</h2>
<p>Whether it is face masks, reading your favourite book, binging the latest series of Sabrina or something else, make sure you make time to care for yourself. Where safe to do so, continue your hobbies or take up new ones such as yoga or meditation.</p>
<p>There are a variety of online tutorials on YouTube for all experience levels and focusing on your breathing can work wonders for any stress. You can even join the tutorial with friends via Skype.</p>
<p><img src="/media/iStock-937330894.jpg" alt="Yellow Brain Psychology, Yellow Brain Psychology thinking image " style="width : 9737px; height : 5134px;     " /></p>
<h2>The most important tip is to be kind</h2>
<p>Be kind to yourself, your loved ones and your community. Reach out to those self-isolating and help with groceries if you are able, smile at your neighbours on your daily walk and follow government guidance to protect those most vulnerable.</p>
<p><img src="/media/Cool-to-be-kind.png" alt="Cool to be kind graphic, it's cool to be kind stock graphic" style="width : 640pxpx; height : 548pxpx;     " /></p>
<p>We want to say a special thank you to the lecturers and students from across all our Institutes that contributed to our top tips for all their help. Make sure you leave your own top tips to a happy mind&nbsp;on social media, tagging @cumbriauni or use the hashtag #cumbriauni&nbsp;to help spread the positivity to our community and beyond.</p>
<h2>Part Two: Advice from our Student Union</h2>
<p><strong><img src="/media/Students-Union-Officers.jpg" alt="Students Union Officers, Lequane, Katie and Joe" style="width : 1479pxpx; height : 1002pxpx;     " /></strong></p>
<p class="xmsonormal"><em>Joseph Morrell- UCSU Welfare and Support Officer (right)</em></p>
<h3 class="xmsonormal">What advice would you give during this stressful time?</h3>
<p class="xmsonormal">For me its really important to maintain a routine- get up, have breakfast, have a shower, make my bed and get out of the bedroom. I think its really easy to fall into the trap of staying in bed to then feeling stressed that nothing has been achieved. At least by doing these small steps I have achieved something which encourages me to be more proactive throughout the day! I do understand, in these highly isolating and anxiety provoking circumstances it may be difficult to achieve these steps so dont be too disheartened if you are unable to (sometimes Im not able to), take your time and take each day as it comes.</p>
<h3 class="xmsonormal">What do you do to remain focused?</h3>
<p class="xmsonormal">If youre planning to study or work, think about the environment that youll be working in. Whilst working from home I have found it highly difficult to remain focused unless I have my own space. I refrain from sitting in the living room and bedroom as I find I get too comfortable and Im more likely to put the TV on or look at my phone and lose focus. In my space I find it helpful to have the radio on (low volume) because Im getting on a bit Im currently listening to Radio 2 This not only takes away the visual stimulus of the TV but it ensures Im not sitting in silence and feel part of a conversation which helps me mentally.</p>
<h3 class="xmsonormal">The most important thing?</h3>
<p class="xmsonormal">That hour you get to spend outside make the most of it. Do not feel as though you have to jump on the bandwagon and go for a run. This is your time and do what you need to do to keep your mind peaceful. Dont get me wrong, I think exercise is so good for the mind but do it your way thats the best way!</p>
<p class="xmsonormal"><em>I wish you all the best and stay safe - Joe</em></p>
<h2 class="xmsonormal">Part Three: Looking for more ways to stay well during isolation?</h2>
<p class="xmsonormal"><em>Our fantastic Mental Health and Wellbeing Manager, David Wilson, has compiled 5 ways to wellbeing during self-isolation. Read a little more about David's work below and follow the link to the blog.</em></p>
